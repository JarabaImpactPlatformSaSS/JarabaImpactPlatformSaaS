// PO FILE UPDATER
// ============================================================================

function fillPO(poPath, translations) {
    let content = fs.readFileSync(poPath, 'utf8');
    let filled = 0;

    for (const [msgid, msgstr] of Object.entries(translations)) {
        const escapedId = msgid.replace(/\\/g, '\\\\').replace(/"/g, '\\"');
        const escapedStr = msgstr.replace(/\\/g, '\\\\').replace(/"/g, '\\"');

        // Match: msgid "..." \n msgstr ""
        const pattern = `msgid "${escapedId}"\nmsgstr ""`;
        const replacement = `msgid "${escapedId}"\nmsgstr "${escapedStr}"`;

        if (content.includes(pattern)) {
            content = content.replace(pattern, replacement);
            filled++;
        }
    }

    fs.writeFileSync(poPath, content, 'utf8');
    return filled;
}

