function main() {
    const enPath = path.join(TRANSLATIONS_DIR, 'ecosistema_jaraba_theme.en.po');
    const ptPath = path.join(TRANSLATIONS_DIR, 'ecosistema_jaraba_theme.pt-br.po');

    console.log('Completing EN translations...');
    const enFilled = fillPO(enPath, COMPLETE_EN);
    console.log(`  Filled ${enFilled} additional EN translations.`);

    console.log('Completing PT-BR translations...');
    const ptFilled = fillPO(ptPath, COMPLETE_PT);
    console.log(`  Filled ${ptFilled} additional PT-BR translations.`);

    // Count remaining empty
    const enContent = fs.readFileSync(enPath, 'utf8');
    const ptContent = fs.readFileSync(ptPath, 'utf8');
    const enEmpty = (enContent.match(/msgstr ""\n/g) || []).length - 1; // -1 for header
    const ptEmpty = (ptContent.match(/msgstr ""\n/g) || []).length - 1;

    console.log(`\nEN: ${enEmpty} strings still untranslated`);
    console.log(`PT: ${ptEmpty} strings still untranslated`);
}

